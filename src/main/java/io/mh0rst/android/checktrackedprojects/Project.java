package io.mh0rst.android.checktrackedprojects;

public class Project {
    String name;
    String path;
    String remote;
    boolean latestFound;
    boolean latestIsEqual;

    public Project(String path, String name) {
        this.name = name;
        this.path = path;
    }

    @Override
    public String toString() {
        return "Project " + name + " on path " + path + " from remote " + remote;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((path == null) ? 0 : path.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Project other = (Project) obj;
        if (path == null) {
            if (other.path != null)
                return false;
        } else if (!path.equals(other.path))
            return false;
        return true;
    }

}
